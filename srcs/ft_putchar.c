#include <unistd.h>
#include "libft.h"

void	ft_putchar(int c)
{
	char	u[4];
	size_t	len;

	len = 1;
	ft_bzero(u, 4);
	u[3] = (c & 0b01111111);
	if (c >= 0x80 && len++)
	{
		u[3] = 0b10000000 | (c & 0b00111111);
		u[2] = 0b11000000 | ((c >> 6) & 0b00011111);
		if (c >= 0x800 && len++)
		{
			u[2] = 0b10000000 | ((c >> 6) & 0b00111111);
			u[1] = 0b11100000 | ((c >> 12) & 0b00001111);
			if (c >= 0x10000 && len++)
			{
				u[1] = 0b10000000 | ((c >> 12) & 0b00111111);
				u[0] = 0b11110000 | ((c >> 18) & 0b00000111);
			}
		}
	}
	write(1, u + (4 - len), len);
}
