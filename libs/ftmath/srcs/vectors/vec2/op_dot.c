#include "ftmath/t_vec2.h"

t_dbl__		v2_dot__(t_vec_gen__ *v1, t_vec_gen__ *v2)
{
	if (v1->t & VEC3 || v2->t != v1->t)
		return (0.0 / 0.0);
	return (g_v2f__[v1->t]->dot(v1, v2));
}
