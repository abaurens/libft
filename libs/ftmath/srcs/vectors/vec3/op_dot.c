#include "ftmath/t_vec3.h"

t_dbl__		v3_dot__(t_vec_gen__ *v1, t_vec_gen__ *v2)
{
	if (!(v1->t & VEC3) || (v1->t & ~VEC3) > LDBL || v1->t != v2->t)
		return (0.0 / 0.0);
	return (g_v3f__[v2->t & ~VEC3]->dot(v1, v2));
}
